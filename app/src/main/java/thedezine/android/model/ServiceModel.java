package thedezine.android.model;



public class ServiceModel {

    private String title,icon,color,id;


    public ServiceModel(String id,String title, String icon, String color) {
        this.title = title;
        this.icon = icon;
        this.color = color;
        this.id=id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
